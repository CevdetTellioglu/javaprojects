package day01variables;

public class Variables02 {

	public static void main(String[] args) {
		
		char harf1 = 'a'; //a nin degeri 97
		char harf2 = 'b'; //b nin degeri 98
		// syso sonra control + space => System.out.println(); 
		
		System.out.println(harf1 + harf2); // cevap: 97+98 verecektir
		System.out.println(harf1); // a 
		System.out.println(harf2); // b
		
		// ab yazdirmak icin asagidaki yontem kullanilabilir.
		System.out.print(harf1); // a 
		System.out.println(harf2); // b
		
		// Bir char in sayi degeri ASCII tablosu olmadan nasil bulunur?
		// 'a' //97 ==> 97 bir tamsayi 
		// Data type'i int olan bir variable olustur ve char'i bu variable'a ata
		int aCharDeger = 'a';
		
		System.out.println(aCharDeger);
	
		
		
		//
		
		int unlemDeger = '!';
		int yuzdeDeger = '%';
		int yildizDeger = '*';
		System.out.println(unlemDeger);
		System.out.println(yuzdeDeger);
		System.out.println(yildizDeger);
		
		
		
		
		
		
		
		
		
		
		

	}

}
