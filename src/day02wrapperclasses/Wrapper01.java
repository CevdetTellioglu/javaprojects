package day02wrapperclasses;

public class Wrapper01 {

	public static void main(String[] args) {

		//int data typenin maximum ve minimum degerlerini bulup ekrana yazdiriniz.
		// int in wrapper class'ini kullancaksiniz
		
		
		System.out.println(Integer.MAX_VALUE); // Bir Class'in icindeki methodalara ulasmak icin class isminden sonra 
												// nokta koy ve listeden secim yap
		
		System.out.println(Integer.MIN_VALUE);
		
		//long datatype max min deger\
		// wrapper ===> cevreleyen 
		
		System.out.println(Long.MAX_VALUE);
		System.out.println(Long.MIN_VALUE);
		
		// ODEV : Tum primitivelerin max ve min degerlerini Wrapper classlearini kullanarak ekrana yazdiriniz
		
		
		
	}

}
